                    required: true,
                },
            },
            submitHandler: function(form) {
                

                var form_data = $('#comapre_form').serialize()
                console.log(form_data);
                $.ajax({
                    type: "POST",
                    dataType: 'json',
                    url: "get_price.php",
                    data: form_data,
                    success: function(data) {

                        function numberWithCommas(x) {
                            return x.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
                        }
                        console.log(data.debt_val); 
                        $('.full_section.sec-section').show();
                        $('.cuurent_price_con').text("$" + numberWithCommas(parseInt(data.debt_val)));
                        $('.cuurent_price').text("$" + data.price.toString().charAt(0) + ',' + data.price.toString().slice(1));
                        $('.current_state').text(data.state);
                    }
                });
            
            setTimeout(function(){ 
                $('html, body').animate({
                    scrollTop: $("#sec-section").offset().top - 56
                }, 2000);
            }, 1500);

            }
        });
            
        });


